"""
一只青蛙一次可以跳上1级台阶，也可以跳上2级。求该青蛙跳上一个 n 级的台阶总共有多少种跳法（先后次序不同算不同的结果）。
0 <= n <= 40
"""

# 输入正整数n
n = int(input())

#
dp = [0] * (n + 1)
# # 1个台阶肯定是一种方法了
# dp[1] = 1
# # 2个台阶可以两种方法 1+1 和 2
# dp[2] = 2
# # 3个台阶可以 111 21 12 三种方法
# dp[3] = 3
# # 4个台阶, 1111 112 121 211 22
# dp[4] = 5

if len(dp) == 1:
    print(0)
elif len(dp) == 2:
    print(1)
elif len(dp) == 3:
    print(2)
else:
    dp[1] = 1
    dp[2] = 2
    for i in range(3, n + 1):
        dp[i] = dp[i - 1] + dp[i - 2]

    print(dp[n])
